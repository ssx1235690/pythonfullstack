# 树的遍历分广度优先和深度优先
# 广度优先   英文缩写为BFS即Breadth
# FirstSearch。其过程检验来说是对每一层节点依次访问，访问完一层进入下一层，而且每个节点只能访问一次

# 深度优先  英文缩写为DFS即Depth First Search.其过程简要来说是对每一个可能的分支路径深入到不能再深入为止，而且每个节点只能访问一次




